public class MathOperators {
    public static void main(String[] args) {

        int x = 13;
        int y = 12;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
        System.out.println(y % x);

        y += 5;

        System.out.println("Nowy Y: " + y);


    }
}
